const NAV = [
  { href: "/tests",    label: "Tests",    Icon: TestsIcon    },
  { href: "/results",  label: "Results",  Icon: ResultsIcon  },
  { href: "/users",    label: "Users",    Icon: UsersIcon    },
  { href: "/settings", label: "Settings", Icon: SettingsIcon },
];

function hasAccessToken() {
  if (typeof document === "undefined") return false;
  return /(?:^|; )access_token=/.test(document.cookie);
}

export default function NavSidebar() {
  const pathname = usePathname();
  const router = useRouter();

  useEffect(() => {
    if (!hasAccessToken()) router.replace("/login");
  }, [pathname]); // eslint-disable-line react-hooks/exhaustive-deps

  function handleLogout() {
    clearAuthCookies();
    router.push("/login");
  }

  return (
    <aside className="w-56 min-h-screen bg-white border-r border-gray-200 flex flex-col">
      <div className="px-5 py-4 border-b border-gray-100">
        <div className="flex items-center gap-2.5">
          <Image src="/icon-64.png" alt="QuizBuilder" width={28} height={28} className="shrink-0" />
          <span className="text-lg font-bold text-gray-900">Quiz<span className="text-amber-500">Builder</span></span>
        </div>
      </div>

      <nav className="flex-1 px-3 py-4 space-y-0.5">
        {NAV.map(({ href, label, Icon }) => {
          const active = pathname === href || pathname.startsWith(href + "/");
          return (
            <Link
              key={href}
              href={href}
              className={`flex items-center gap-3 px-3 py-2 rounded-lg text-sm font-medium transition-colors ${
                active
                  ? "bg-amber-50 text-amber-700 border-l-2 border-amber-500 rounded-l-none pl-[10px]"
                  : "text-gray-500 hover:bg-gray-50 hover:text-gray-900"
              }`}
            >
              <span className={active ? "text-amber-500" : "text-gray-400"}>
                <Icon />
              </span>
              {label}
            </Link>
          );
        })}
      </nav>

      <div className="px-3 py-4 border-t border-gray-100">
        <button
          onClick={handleLogout}
          className="w-full flex items-center gap-3 px-3 py-2 text-sm text-gray-400 hover:text-gray-700 rounded-lg hover:bg-gray-50 transition-colors"
        >
          <svg className="w-4 h-4" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={1.8}>
            <path strokeLinecap="round" strokeLinejoin="round"
              d="M17 16l4-4m0 0l-4-4m4 4H7m6 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h4a3 3 0 013 3v1" />
          </svg>
          Sign out
        </button>
      </div>
    </aside>
  );
}
